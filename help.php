        <h2>Wie integriere ich die Suchfunktion in meine Wordpress Website?</h2>
        <p>Erm&ouml;glichen Sie dem Endkunden eine bequeme usability durch die einfache Integration der Suchfunktion in Ihre Wordpress Website.</p>
        <p><?php echo '<img src="' . esc_url(plugins_url('assets/search-demo.png', __FILE__)) . '" alt="" > '; ?></p>
        <h3>Anleitung zur Integration der Suchfunktion</h3>
        <ol class="ol-decimal">
            <li>
                <h4>Plugin Installation</h4>
                <ul class="ul-disc">
                    <li>Logen Sie sich in Ihren Wordpress Admin Account ein</li>
                    <li>Klicken Sie in der linken Men&uuml;spalte auf <strong>Plugin ? Add new</strong>  und suchen Sie nach <strong>Onlineshop by Brodos</strong></li>
                    <li>Als Suchergebnis sollte nun <strong>Onlineshop by Brodos</strong> erscheinen. Klicken Sie rechts neben dem <strong>Onlineshop by Brodos</strong> Icon auf <strong>Jetzt installieren</strong>. Die Installation kann einige Minuten dauern.</li>
                    <li>Klicken Sie nun auf den Button <strong>Aktivieren</strong> neben dem Brodos Plugin Icon.</li>
                    <li>Tragen Sie nun wie beschrieben Ihre <strong>Shop-URL</strong> in das leere Feld ein (z. B modehaus.brodos.net). </li>
                    <li>Schlie&szlig;en Sie den Vorgang mit <strong>speichern</strong> ab. Die erfolgreiche Installation wird Ihnen mit dem Hinweis <em>&bdquo;Your setting have been updated!&ldquo;</em> best&auml;tigt.</li>
                </ul>
            </li>
            <li>
                <h4>Integration der Suchleiste auf Ihrer Wunschseite</h4>
                <ul class="ul-disc">
                    <li>W&auml;hlen Sie in der linken Men&uuml;spalte unter <strong>Pages</strong> Ihre Wunschseite aus, auf der die Suchleiste angezeigt werden soll. </li>
                    <li>
                        F&uuml;r die Seitenbearbeitung &uuml;ber <strong>Elementor</strong>, w&auml;hlen Sie den Texteditor &uuml;ber drag and drop aus.<br/>
                        <ul class="ul-square">
                            <li>Es &ouml;ffnet sich nun auf der linken Seite der Elementor <strong>Textbearbeitungsbereich</strong>.</li>
                            <li>Bitte f&uuml;gen Sie hier den <strong>Shortcode [BrodosSearch]</strong> in den gew&uuml;nschten Bereich ein.</li>
                        </ul>
                    </li>
                    <li>F&uuml;r die Seitenbearbeitung &uuml;ber den <strong>Standard-Wordpress-Page-Builder</strong>, w&auml;hlen Sie den Textbearbeitungsmodus aus und f&uuml;gen Sie den in Punkt 2 aufgef&uuml;hrten Shortcode ein.</li>
                    <li>Das Plugin passt sich automatisch der Website css an.</li>
                </ul>
            </li>
        </ol>
    </article>
